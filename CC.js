let baseurl = "https://cdn.jsdelivr.net/npm/@fawazahmed0/currency-api@latest/v1/currencies";
let dropdowns = document.querySelectorAll(".dropdown select");
const btn = document.querySelector("form button");
const res = document.querySelector(".msg");

for(let select of dropdowns){
    for(let curcode in countryList){
        let opt = document.createElement("option");
        opt.innerText = curcode;
        opt.value = curcode;
        if (select.name==="from"&& curcode==="USD") {
            opt.selected="selected";
        } 
        else if(select.name==="to" && curcode==="INR"){
            opt.selected="selected";
        }
        select.append(opt);
    }
    select.addEventListener("change",(evt)=>{
        updateFlag(evt.target);
    })
}


const updateFlag = (elem)=>{
    let curcode = elem.value;
    let countrycode = countryList[curcode];
    let flagimg = elem.parentElement.querySelector("img");
    if (flagimg) {
        flagimg.src = `https://flagsapi.com/${countrycode}/flat/64.png`;
    }
}



btn.addEventListener("click", async (evt) => {
    evt.preventDefault();

    let amount = document.querySelector(".amount input");
    let amtval = amount.value.trim();
    if (amtval === "" || amtval <= 0) {
        amtval = 1;
    }

    let fromCurrency = document.querySelector(".dropdown select[name='from']").value;
    let toCurrency = document.querySelector(".dropdown select[name='to']").value;

    let url = `${baseurl}/${fromCurrency.toLowerCase()}.json`;
    let response = await fetch(url);
    let data = await response.json();
    let convrate = data[fromCurrency.toLowerCase()][toCurrency.toLowerCase()];
    let convamt = amtval*convrate;
    res.innerText = `${amtval} ${fromCurrency} = ${convamt.toFixed(2)} ${toCurrency}`;
});
